//0.	DO NOT use reserved keywords (int, double, float, main, char, etc.)

//1.	use explicit names, that describes the variable or its usage

//2.	you can never begin a name with a number

//3.	a name can have no spaces, nor special characters (+,=,-,*,@,#,?,",etc.)

//4.	you can use "_" in place of spaces or use hungarian or CamelCase

//5.	names are case sensitive


